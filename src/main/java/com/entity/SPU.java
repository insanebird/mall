package com.entity;

public class SPU {
    private int spuId;
    private String spuName;
    private int spuCode;
    private String spuBulletin;
    private String spuDetail;
    private int spuCategoryId;
    private int spuBrandId;

    public int getSpuId() {
        return spuId;
    }

    public void setSpuId(int spuId) {
        this.spuId = spuId;
    }

    public String getSpuName() {
        return spuName;
    }

    public void setSpuName(String spuName) {
        this.spuName = spuName;
    }

    public int getSpuCode() {
        return spuCode;
    }

    public void setSpuCode(int spuCode) {
        this.spuCode = spuCode;
    }

    public String getSpuBulletin() {
        return spuBulletin;
    }

    public void setSpuBulletin(String spuBulletin) {
        this.spuBulletin = spuBulletin;
    }

    public String getSpuDetail() {
        return spuDetail;
    }

    public void setSpuDetail(String spuDetail) {
        this.spuDetail = spuDetail;
    }

    public int getSpuCategoryId() {
        return spuCategoryId;
    }

    public void setSpuCategoryId(int spuCategoryId) {
        this.spuCategoryId = spuCategoryId;
    }

    public int getSpuBrandId() {
        return spuBrandId;
    }

    public void setSpuBrandId(int spuBrandId) {
        this.spuBrandId = spuBrandId;
    }
}
